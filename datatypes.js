// Data types define different forms of data that can be store or manipulated.
// in js data types include string, number, boolean, object, null, undefined,array

// string - a sequence of characters used to represent text and can be created using single or double quotes

let message = "Hello World!!!!"

// number - a numeric value, which can be an integer or a floating-point number

let rate = 26.7
let count = 900

// boolean - a data type that can hold one of two values: true or false

let isAngry = true
let isLoggedIn = false

// object - an unordered collection of key-value pairs, where keys are strings and values can be of any data type. Objects can be created using curly braces.

let animal = {
    name: "cat",
    noOfLegs: 4,
    type: "mammal",
    isDomestic: true,
    color: "black"
}
// array - a collection/list of elements, which can hold multiple values of any data type. Arrays can be created using square brackets.

let fruits = ["apple", "banana", "cherry"];

// null - represents the intentional absence of any object value and is a primitive data type.
let gameScore = null

// undefined - a data type that indicates a variable has been declared but has not yet been assigned a value.
let playerName;

